package UD04_act2;

public class UD04_act2 {

	public static void main(String[] args) {
		int N = 5;
		double A = 4.3; 
		Character C = 84; //character en ASCII --> T
		System.out.println("Valor N es: " + N );
		System.out.println("Valor A es: " + A);
		System.out.println("Valor C es: " + C);
		System.out.println("Valor N + A es: "+ (N+A));
		System.out.println("Valor A - N es: "+ (A-N));
		System.out.println("Valor N + A es: "+ Integer.valueOf(C));
	}

}
